package me.hapyl.eterna.module.npc.appearance;

/**
 * Represents a {@link AppearanceVillager} level, indicated via a badge.
 */
public enum VillagerLevel {
    
    /**
     * Novice level; stone badge.
     */
    NOVICE,
    
    /**
     * Apprentice level; iron badge.
     */
    APPRENTICE,
    
    /**
     * Journeyman level; golden badge.
     */
    JOURNEYMAN,
    
    /**
     * Expert level; emerald badge.
     */
    EXPERT,
    
    /**
     * Master level; diamond badge.
     */
    MASTER
    
}
